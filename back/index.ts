const sumar = (a: number, b: number): number => {  
    return a + b; 
}

const restares = (a: number, b: number): number => { 
    return a - b; 
} 

const multiplicar = (a: number, b: number): number => { 
    return a * b; 
} 

const dividir = (a: number, b: number): number => { 
    return a / b; 
} 

sumar(1, 2); // 3 
restares(1, 2); // -1 
multiplicar(1, 2); // 2  
dividir(1, 2); // 0.5    